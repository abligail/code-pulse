// Client-only icon wrapper to avoid hydration mismatches with browser extensions
function ClientIcon({
  icon: Icon,
  className
}: {
  icon: React.ElementType;
  className?: string;
}) {
  const [isMounted, setIsMounted] = React.useState(false);

  React.useEffect(() => {
    setIsMounted(true);
  }, []);

  if (!isMounted) {
    return <div className={className} />;
  }

  return <Icon className={className} />;
}

function Select({
  children,
  defaultValue,
  value,
  onValueChange,
  ...props
}: React.ComponentProps<typeof SelectPrimitive.Root>) {
  const [isMounted, setIsMounted] = React.useState(false);
  const [clientValue, setClientValue] = React.useState(value || defaultValue);

  React.useEffect(() => {
    setIsMounted(true);
  }, []);

  // Sync the value from props when mounted
  React.useEffect(() => {
    if (value !== undefined) {
      setClientValue(value);
    }
  }, [value]);

  // Return a placeholder during SSR
  if (!isMounted) {
    return (
      <div className="pointer-events-none flex min-w-[120px] items-center gap-2 rounded-xl border border-input/85 bg-card/76 px-3 py-2 text-sm opacity-50 shadow-[0_10px_20px_-18px_rgba(15,23,42,0.35)]">
        <div className="flex-1">{value || defaultValue || ''}</div>
        <ClientIcon icon={ChevronDownIcon} className="size-4 opacity-50" />
      </div>
    );
  }

  return (
    <SelectPrimitive.Root
      data-slot="select"
      value={clientValue}
      onValueChange={(val) => {
        setClientValue(val);
        if (onValueChange) {
          onValueChange(val);
        }
      }}
      {...props}
    >
      {children}
    </SelectPrimitive.Root>
  );
}

